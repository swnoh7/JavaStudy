package JF3일예제;

import java.util.Scanner;

public class Javafestival_4 {

	public static void main(String[] args) {

		Scanner sc = new Scanner(System.in);
		
		System.out.print("n 입력 : ");
		
		int num = sc.nextInt();
		int sum = 1;
		
		for(int i = 0; i < num; i++) {
			sum = sum + i;
			System.out.print(sum + " ");
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
