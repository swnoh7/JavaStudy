package java복습;

import java.util.Scanner;

public class JAFE연습 {

	public static void main(String[] args) {

		Scanner sc = new Scanner(System.in);
		
		System.out.print("입력 : ");
		int n = sc.nextInt();
		
		int gop = 1;
		
		for (int i = n; i > 0; i--) {
			gop *= i;
		}
		
		System.out.println("출력 : " + gop);
		
		
		
		
		
		
		
		
		
		
		
		
		
	}
}
